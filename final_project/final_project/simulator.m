function [P1, P2, V] = simulator()
global e1 e2 e3 e4 e5 U1 V1 U2 V2 n;
%first calibrate information
    rx=0; ry=90* pi; rz=180* pi;
    Rx=[1 0 0;
        0 cos(rx) -sin(rx);
        0 sin(rx)  cos(rx)];
    Ry=[cos(ry) 0 sin(ry);
        0 1 0;
        -sin(ry) 0 cos(ry)];
    Rz=[cos(rz) -sin(rz) 0;
        sin(rz)  cos(rz) 0;
        0 0 1];
    R1=Rz*Ry*Rx;
    
    tx=250; ty=500; tz=1000;
    T1=[tx;ty;tz];
    RT1=[R1 T1;
        0 0 0 1];
       
    A1=[1 0 1 0;
        0 1 1 0;
        0 0 1 0];   
    P1=A1*RT1;
    
%second calibrate information    
    rx=0;ry= 0;rz=0;
    
    Rx=[1 0 0;
        0 cos(rx) -sin(rx);
        0 sin(rx)  cos(rx)];
    Ry=[cos(ry) 0 sin(ry);
        0 1 0;
        -sin(ry) 0 cos(ry)];
    Rz=[cos(rz) -sin(rz) 0;
        sin(rz)  cos(rz) 0;
        0 0 1];
    R2=Rz*Ry*Rx;
    
    tx=1000;ty=-400;tz=0;
    T2=[tx;ty;tz];
    RT2=[R2 T2;
        
        0 0 0 1];
    A2=[1 0 1 0;
        0 1 1 0;
        0 0 1 0];
    P2=A2*RT2;
    
    n = 33
    
    V(:,1) = [1100;500;500;1];
    V(:,2) = [500;1100;500;1];
    V(:,3) = [500;500;1100;1];
    V(:,4) = [500;500;500;1];
    V(:,5) = [1100;1100;1100;1];


    V(:,6) = [500;700;500;1];
    V(:,7) = [500;700;700;1];
    V(:,8) = [500;700;900;1];
    V(:,9) = [500;700;1100;1];

    V(:,10) = [500;900;500;1];
    V(:,11) = [500;900;700;1];
    V(:,12) = [500;900;900;1];
    V(:,13) = [500;900;1100;1];

    V(:,14) = [700;500;500;1];
    V(:,15) = [700;500;700;1];
    V(:,16) = [700;500;900;1];
    V(:,17) = [700;500;1100;1];

    V(:,18) = [900;500;500;1];
    V(:,19) = [900;500;700;1];
    V(:,20) = [900;500;900;1];
    V(:,21) = [900;500;1100;1];


    V(:,22) = [500;500;700;1];
    V(:,23) = [500;500;900;1];

    V(:,24) = [700;700;1100;1];
    V(:,25) = [900;900;1100;1];

    V(:,26) = [500;1100;700;1];
    V(:,27) = [500;1100;900;1];

    V(:,28) = [1100;500;700;1];
    V(:,29) = [1100;500;900;1];
    V(:,30) = [700;900;1100;1];
    V(:,31) = [900;700;1100;1];
    V(:,32) = [1100;500;1100;1];
    V(:,33) = [500;1100;1100;1];
    
    for i=1:n,
        m1(:,i) = P1*V(:,i);
        U1(i) = m1(1,i)/m1(3,i);
        V1(i) = m1(2,i)/m1(3,i);
        m2(:,i) = P2*V(:,i);
        U2(i) = m2(1,i)/m2(3,i);
        V2(i) = m2(2,i)/m2(3,i);
    end
    
    
end